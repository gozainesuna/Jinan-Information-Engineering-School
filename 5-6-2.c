//例题5-6-2
/*
	已有一个排好顺序的数列，输入一个数插入到该数列中，使之仍然保持有序。
	例如：将15插入到有序数列{3，7，10，12，18，20}
							a[i] a[a+1]
	a[0] a[1] a[2] a[3] a[4] a[5] a[6]
	3	 7	  10   12   18   20 
	7	 7	  10   12        18   20
*/
#include<stdio.h>
void main()
{
	int a[7]={3,7,10,12,18,20};
	int x=15,i=5;
	
	while( i>=0 )
	{
		if( a[i]>x )
			a[i+1]=a[i];
		else
			break;
		i--;
	}
	a[i+1]=x;
	for( i=0; i<=6; i++ )
	{
		printf("%4d",a[i]);
	}
}