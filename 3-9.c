#include <stdio.h>

void main()
{
    int m; // 月份
    int n; // 数量
    int r; // 优惠率

    printf("请输入月份(m)和数量(n)：");
    scanf("%d", &m);
//----------------------------------------------------------------
    if( m==6 || m==12)
    {
    	r = 10;
	}
	
	else
	{
		scanf("%d",&n);
	}
//----------------------------------------------------------------
    if ( m>=7 && m<=9 )
    {
		if( n>=20 )
		{
			r = 15;
		} 
		
		else if( n<20 && n>=1 )
		{
			r = 5;
		}
    }
//----------------------------------------------------------------    
	else if ( m>=1 && m<=5 || m==10 || m==11 )
    {
        if( n>=20 )
		{
			r = 30;
		} 
		
		else if( n<20 && n>=1 )
		{
			r = 20;
		}
    }
//----------------------------------------------------------------    
	else if ( m<1 || m>12)
    {
    	printf("月份输入错误");
	}
	
	printf("优惠率为 %d%%",r);

}